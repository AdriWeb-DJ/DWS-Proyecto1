package dto;

public class ClubDto {	
	private long idClub;
	private String nombreClub = "aaaaa";
	private String descClub = "aaaaa";
	private String enlaceImagenClub = "aaaaa";
	private String sedeClub = "aaaaa";
	
	/**
	 * @return the idClub
	 */
	public long getIdClub() {
		return idClub;
	}
	/**
	 * @param idClub the idClub to set
	 */
	public void setIdClub(long idClub) {
		this.idClub = idClub;
	}
	/**
	 * @return the nombreClub
	 */
	public String getNombreClub() {
		return nombreClub;
	}
	/**
	 * @param nombreClub the nombreClub to set
	 */
	public void setNombreClub(String nombreClub) {
		this.nombreClub = nombreClub;
	}
	/**
	 * @return the descClub
	 */
	public String getDescClub() {
		return descClub;
	}
	/**
	 * @param descClub the descClub to set
	 */
	public void setDescClub(String descClub) {
		this.descClub = descClub;
	}
	/**
	 * @return the enlaceImagenClub
	 */
	public String getEnlaceImagenClub() {
		return enlaceImagenClub;
	}
	/**
	 * @param enlaceImagenClub the enlaceImagenClub to set
	 */
	public void setEnlaceImagenClub(String enlaceImagenClub) {
		this.enlaceImagenClub = enlaceImagenClub;
	}
	/**
	 * @return the sedeClub
	 */
	public String getSedeClub() {
		return sedeClub;
	}
	/**
	 * @param sedeClub the sedeClub to set
	 */
	public void setSedeClub(String sedeClub) {
		this.sedeClub = sedeClub;
	}
}
